package question100.ex1;

public class Student {
    private String no;
    private String name;

    public Student(String no, String name) {
        this.no = no;
        this.name = name;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getNo(){
        return this.no;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }





}
